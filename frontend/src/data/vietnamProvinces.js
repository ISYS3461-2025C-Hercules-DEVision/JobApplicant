// normalize để so khớp "không phân biệt dấu/hoa/thừa khoảng trắng"
export function normalizeVietnamese(str = "") {
  return String(str)
    .trim()
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "") // remove diacritics
    .replace(/đ/g, "d")
    .replace(/\s+/g, " ");
}

export function isValidVietnamProvince(input) {
  const n = normalizeVietnamese(input);
  if (!n) return false;

  return VIETNAM_PROVINCES.some((p) => normalizeVietnamese(p) === n);
}
